// Declare a variable called 'name' and assign it the value "Ruan"
let name = "Ruan";

// Check if the name is exactly "Ruan"
if (name === "Ruan") {
   // If true, print a greeting for yourself
   console.log("My name:", "Hello Me!");

} else if (name === "John") {
   // If the name is "John", print a greeting for Dad
   console.log("Dad's Name:", "Hello John!");

} else if (name === "Jane") {
   // If the name is "Jane", print a greeting for Mom
   console.log("Mom's Name:", "Hello Jane!");

} else {
   // If the name doesn't match any of the above, greet a stranger
   console.log("Hello Stranger!");
}
