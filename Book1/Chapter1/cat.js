// Define a function called drawCats that takes one parameter
// howManyTimes determines how many cats will be drawn
var drawCats = function (howManyTimes) {

   // Loop from 0 up to (but not including) howManyTimes
   for (var i = 0; i < howManyTimes; i++) {

      // Print the current number and a cat face to the console
      console.log(i + " =^.^=");
   }
};

// Call the drawCats function and draw 10 cats
drawCats(10);



// Start a loop with i set to 8; keep looping while i is less than 11; increase i by 1 each time
for (var i = 8; i < 11; i++) {

   // Print the current value of i followed by a cute face to the console
   console.log(i + " =^.^=");
}

// End of the loop

